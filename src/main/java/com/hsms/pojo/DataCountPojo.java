package com.hsms.pojo;

public class DataCountPojo {
	
	
	private double purchaseCount;
	private double saleCount;
	private double SumCount;
	private double purchaseCountPercent;
	private double saleCountPercent;
	public double getPurchaseCount() {
		return purchaseCount;
	}
	public void setPurchaseCount(double purchaseCount) {
		this.purchaseCount = purchaseCount;
	}
	public double getSaleCount() {
		return saleCount;
	}
	public void setSaleCount(double saleCount) {
		this.saleCount = saleCount;
	}
	public double getSumCount() {
		return SumCount;
	}
	public void setSumCount(double sumCount) {
		SumCount = sumCount;
	}
	public double getPurchaseCountPercent() {
		return purchaseCountPercent;
	}
	public void setPurchaseCountPercent(double purchaseCountPercent) {
		this.purchaseCountPercent = purchaseCountPercent;
	}
	public double getSaleCountPercent() {
		return saleCountPercent;
	}
	public void setSaleCountPercent(double saleCountPercent) {
		this.saleCountPercent = saleCountPercent;
	}
   
}
